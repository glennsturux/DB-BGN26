FROM EMP AS e
INNER JOIN DEPT AS d 
ON e.DEPTNO = d.DEPTNO
WHERE d.DEPTNO IN (
  SELECT DEPTNO
  FROM EMP
  WHERE JOB = 'MANAGER' AND SAL > (
    SELECT SAL
    FROM EMP
    WHERE ENAME = 'BLAKE'
  )
);

