SELECT E.ENAME, D.DNAME
FROM EMP E
LEFT JOIN DEPT D
ON E.DEPTNO = D.DEPTNO;

--2
SELECT E.ENAME, D.DNAME
FROM EMP As E
inner JOIN DEPT As D
ON E.DEPTNO = D.DEPTNO;

--3
SELECT 
    EMP1.ENAME AS Nama_Karyawan, 
    EMP1.SAL AS Gaji_Tertinggi, 
    DEPT.DNAME AS Nama_Departemen
FROM EMP AS EMP1
JOIN DEPT 
	ON EMP1.DEPTNO = DEPT.DEPTNO
WHERE EMP1.SAL = (
    SELECT MAX(EMP2.SAL)
    FROM EMP AS EMP2
    WHERE EMP2.DEPTNO = EMP1.DEPTNO
)
ORDER BY SAL DESC;

--4
SELECT ENAME, JOB, SAL
FROM EMP
WHERE JOB <> 'CLERK' 
  AND SAL > (SELECT AVG(SAL) FROM EMP WHERE JOB = 'ANALYST');

